#CLOUD
def cloud(radius,x,y):
    t.penup()
    t.setpos(x,y)
    t.pendown()
    t.color("light blue","white")
    t.rt(90)

    for i in range(2):
        t.begin_fill()
        t.circle(radius)
        t.end_fill()
        t.lt(90)
    t.begin_fill()
    t.circle(radius,270)
    t.fd(radius*2)
    t.end_fill()
cloud(40,-10,300)
